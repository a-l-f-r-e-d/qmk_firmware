bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case OS_SS:
            if (record->event.pressed) {
                register_code(KC_LCTL);
                register_code(KC_LGUI);
                register_code(KC_LSFT);
                tap_code(KC_4);
                unregister_code(KC_LCTL);
                unregister_code(KC_LGUI);
                unregister_code(KC_LSFT);
            }
            break;
        case OS_NW:
            if (record->event.pressed) {
                register_code(KC_LALT);
                tap_code(KC_RGHT);
                unregister_code(KC_LALT);
            }
            break;
        case OS_PW:
            if (record->event.pressed) {
                register_code(KC_LALT);
                tap_code(KC_LEFT);
                unregister_code(KC_LALT);
            }
            break;
        case ID_NT:
            if (record->event.pressed) {
                register_code(KC_LCTL);
                tap_code(KC_RGHT);
                unregister_code(KC_LCTL);
            }
            break;
        case ID_PT:
            if (record->event.pressed) {
                register_code(KC_LCTL);
                tap_code(KC_LEFT);
                unregister_code(KC_LCTL);
            }
            break;
        case ID_SR:
            if (record->event.pressed) {
                register_code(KC_LCTL);
                register_code(KC_LGUI);
                register_code(KC_LALT);
                tap_code(KC_F1);
                unregister_code(KC_LCTL);
                unregister_code(KC_LGUI);
                unregister_code(KC_LALT);
            }
            break;
        case ID_SU:
            if (record->event.pressed) {
                register_code(KC_LGUI);
                register_code(KC_LALT);
                tap_code(KC_F7);
                unregister_code(KC_LALT);
                unregister_code(KC_LGUI);
            }
            break;
        case ID_STR:
            if (record->event.pressed) {
                register_code(KC_LGUI);
                tap_code(KC_F12);
                unregister_code(KC_LGUI);
            }
            break;
        case ID_RN:
            if (record->event.pressed) {
                register_code(KC_LSFT);
                tap_code(KC_F6);
                unregister_code(KC_LSFT);
            }
            break;
        case ID_NAV:
            if (record->event.pressed) {
                register_code(KC_LGUI);
                tap_code(KC_UP);
                unregister_code(KC_LGUI);
            }
            break;
    }
    return true;
}

